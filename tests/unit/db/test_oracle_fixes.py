class TestOracleFlushPLSQL:
    """Verify flush_oracle preserves PL/SQL blocks."""

    def test_begin_end_block_preserved(self) -> None:
        gen = DialectSQL("oracle")
        gen._buffer.write(
            "BEGIN\n"
            "EXECUTE IMMEDIATE 'CREATE TABLE t (id INTEGER)';\n"
            "EXCEPTION WHEN OTHERS THEN\n"
            "IF SQLCODE != -955 THEN RAISE; END IF;\n"
            "END;\n\n/\n\n"
        )
        result = gen.flush()
        assert len(result) == 1
        assert "BEGIN" in result[0]
        assert "END" in result[0]
        assert "EXECUTE IMMEDIATE" in result[0]
        assert "EXCEPTION" in result[0]

    def test_multiple_statements_split_on_slash(self) -> None:
        gen = DialectSQL("oracle")
        gen._buffer.write(
            "CREATE TABLE a (id INTEGER)\n\n/\n\n"
            "CREATE TABLE b (id INTEGER)\n\n/\n\n"
        )
        result = gen.flush()
        assert len(result) == 2
        assert "CREATE TABLE a" in result[0]
        assert "CREATE TABLE b" in result[1]

    def test_empty_buffer_returns_empty(self) -> None:
        gen = DialectSQL("oracle")
        assert gen.flush() == []

    def test_internal_semicolons_preserved(self) -> None:
        gen = DialectSQL("oracle")
        gen._buffer.write(
            "BEGIN\n"
            "  IF 1 = 1 THEN\n"
            "    EXECUTE IMMEDIATE 'DROP TABLE t';\n"
            "  END IF;\n"
            "EXCEPTION WHEN OTHERS THEN\n"
            "  IF SQLCODE != -942 THEN RAISE; END IF;\n"
            "END;\n\n/\n\n"
        )
        result = gen.flush()
        assert len(result) == 1
        assert ";" in result[0]


