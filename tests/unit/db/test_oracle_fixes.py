class TestOracleJSONSerializers:
    """Verify the staticmethod fix prevents bound-method bug."""

    def test_json_serializer_is_not_bound_method(self) -> None:
        if OracleDialectAsync_oracledb is None:
            pytest.skip(_ORACLEDB_SKIP_REASON)
        OracleDialectAsync_oracledb._json_serializer = staticmethod(
            json.dumps
        )
        d = OracleDialectAsync_oracledb()
        serializer = d._json_serializer
        assert not hasattr(serializer, "__self__")
        result = serializer({"key": "value"})
        assert json.loads(result) == {"key": "value"}

    def test_json_deserializer_is_not_bound_method(self) -> None:
        if OracleDialectAsync_oracledb is None:
            pytest.skip(_ORACLEDB_SKIP_REASON)
        OracleDialectAsync_oracledb._json_deserializer = staticmethod(
            json.loads
        )
        d = OracleDialectAsync_oracledb()
        deserializer = d._json_deserializer
        assert not hasattr(deserializer, "__self__")
        result = deserializer('{"key": "value"}')
        assert result == {"key": "value"}

    def test_plain_function_becomes_bound_method(self) -> None:
        """Verify that without staticmethod, the bug reproduces."""
        if OracleDialectAsync_oracledb is None:
            pytest.skip(_ORACLEDB_SKIP_REASON)
        OracleDialectAsync_oracledb._test_serializer = json.dumps
        d = OracleDialectAsync_oracledb()
        serializer = d._test_serializer
        assert hasattr(serializer, "__self__")
        with pytest.raises(TypeError, match="positional argument"):
            serializer({"key": "value"})
        del OracleDialectAsync_oracledb._test_serializer


