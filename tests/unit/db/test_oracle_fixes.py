class TestFKColspecFormat:
    """Verify FK references include column name (table.column)."""

    def test_fk_includes_column_name(self) -> None:
        gen = DialectSQL("postgresql")
        columns = [
            {
                "name": "id", "type": "INTEGER",
                "primary_key": True, "autoincrement": True,
                "nullable": False,
            },
            {
                "name": "author_id", "type": "INTEGER",
                "target_table": "users", "nullable": False,
            },
        ]
        gen.create_table("posts", columns)
        # For non-SQLite vendors, FKs are deferred (ALTER TABLE ADD CONSTRAINT),
        # not inline in CREATE TABLE, to avoid MSSQL cascade-cycle errors.
        fk_stmts = gen.deferred_fk_stmts(
            "posts",
            [{"name": "author_id", "target_table": "users"}],
        )
        ddl = " ".join(fk_stmts)
        assert "users" in ddl
        assert "FOREIGN KEY" in ddl or "REFERENCES" in ddl

    def test_fk_with_custom_target_column(self) -> None:
        # SQLite keeps inline FKs because it cannot ALTER TABLE ADD CONSTRAINT.
        gen = DialectSQL("sqlite")
        col = gen.build_sa_column({
            "name": "parent_id",
            "type": "INTEGER",
            "target_table": "categories",
            "target_column": "uuid",
            "nullable": True,
        })
        fks = list(col.foreign_keys)
        assert len(fks) == 1
        assert "categories.uuid" in str(fks[0])


class TestColumnCaseNormalization:
    """Verify Oracle UPPERCASE column names are handled."""

    def test_case_normalization_lowercases_introspected_cols(self) -> None:
        upper_cols = frozenset({"ID", "NAME", "EMAIL"})
        normalized = frozenset(c.lower() for c in upper_cols)
        table_cols = ["id", "name", "email"]
        result = [c for c in table_cols if c.lower() in normalized]
        assert result == ["id", "name", "email"]

    def test_case_mismatch_produces_empty_without_normalization(self) -> None:
        upper_cols = frozenset({"ID", "NAME"})
        table_cols = ["id", "name"]
        result = [c for c in table_cols if c in upper_cols]
        assert result == []


class TestRawSqlOnlyDefer:
    """Verify raw_sql() handles only/defer with FK field resolution."""

    @pytest.fixture(autouse=True)
    def setup_model(self) -> None:
        suffix = uuid_mod.uuid4().hex[:8]
        author_table = f"test_raw_sql_authors_{suffix}"
        item_table = f"test_raw_sql_items_{suffix}"

        self.metadata = get_metadata()
        self.table = sa.Table(
            item_table, self.metadata,
            sa.Column("id", sa.Integer, primary_key=True),
            sa.Column("name", sa.String(100)),
            sa.Column("description", sa.Text),
            sa.Column("author_id", sa.Integer,
                      sa.ForeignKey(f"{author_table}.id")),
        )

        class Author(Model):
            name = CharField(max_length=100)

            class Meta:
                table_name = author_table

        class Item(Model):
            name = CharField(max_length=100)
            description = CharField(max_length=500)
            value = IntegerField()
            author = ForeignKey(Author)

            class Meta:
                table_name = item_table

        self.Item = Item
        self.Author = Author

    def test_only_with_fk_field_resolves_to_column(self) -> None:
        qs = QuerySet(self.Item).only("author")
        sql = qs.raw_sql()
        assert "author_id" in sql
        assert "id" in sql

    def test_defer_with_fk_field_resolves_to_column(self) -> None:
        qs = QuerySet(self.Item).defer("author")
        sql = qs.raw_sql()
        assert "author_id" not in sql
        assert "name" in sql


class TestRawSqlFeatures:
    """Verify raw_sql() handles excludes, Q objects, for_update."""

    @pytest.fixture(autouse=True)
    def setup_model(self) -> None:
        self.Q = Q
        suffix = uuid_mod.uuid4().hex[:8]
        tbl = f"test_raw_sql_features_{suffix}"
        self.metadata = get_metadata()
        self.table = sa.Table(
            tbl, self.metadata,
            sa.Column("id", sa.Integer, primary_key=True),
            sa.Column("name", sa.String(100)),
            sa.Column("status", sa.String(50)),
        )

        class FeatureModel(Model):
            name = CharField(max_length=100)
            status = CharField(max_length=50)

            class Meta:
                table_name = tbl

        self.FeatureModel = FeatureModel

    def test_raw_sql_with_exclude(self) -> None:
        qs = QuerySet(self.FeatureModel).exclude(name="bar")
        sql = qs.raw_sql()
        assert "!=" in sql or "<>" in sql

    def test_raw_sql_with_q_object(self) -> None:
        qs = QuerySet(self.FeatureModel).filter(
            self.Q(name="foo") | self.Q(name="bar")
        )
        sql = qs.raw_sql()
        assert "OR" in sql.upper()

    def test_raw_sql_with_for_update(self) -> None:
        qs = QuerySet(self.FeatureModel).filter(id=1).select_for_update()
        sql = qs.raw_sql()
        assert "FOR UPDATE" in sql.upper()

    def test_raw_sql_with_for_update_nowait(self) -> None:
        qs = QuerySet(self.FeatureModel).filter(id=1).select_for_update(
            nowait=True
        )
        sql = qs.raw_sql()
        assert "FOR UPDATE" in sql.upper()
        assert qs._for_update_nowait is True

    def test_raw_sql_with_for_update_skip_locked(self) -> None:
        qs = QuerySet(self.FeatureModel).filter(id=1).select_for_update(
            skip_locked=True
        )
        sql = qs.raw_sql()
        assert "FOR UPDATE" in sql.upper()
        assert qs._for_update_skip_locked is True

    def test_raw_sql_with_complex_lookup(self) -> None:
        qs = QuerySet(self.FeatureModel).filter(name__icontains="foo")
        sql = qs.raw_sql()
        assert "LIKE" in sql.upper()
        assert ":name_1" in sql


